//
// Omega in units of pi so omega=pi is at nyquist limit.
//
void add_discontinuity0w(struct BandLimited *limited, double offset,
                         double omega, double x) {
    //printf("disc0w: offset = %f omega = %f x= %f\n", offset, omega, x);
    double table_offset = 128+8*(-15-offset);
    int ioffset = (int)table_offset;
    double foffset = table_offset-ioffset;
    double row_offset = 16*omega;
    int irow = (int)row_offset;
    double frow = row_offset-irow;
    //printf("ioffset=%d row_offset=%d foffset=%f\n", ioffset, irow, foffset);
    for (int i = -15; i <= 16; ++i) {
        double tab00, tab01, tab10, tab11;

        if (ioffset == 128) {
            tab00 = -0.5;
            tab10 = -0.5;
        } else {
            tab00 = cwtable0[irow][ioffset];
            tab10 = cwtable0[irow+1][ioffset];
        }
        if (ioffset+1 == 128) {
            tab01 = 0.5;
            tab11 = 0.5;
        } else {
            tab01 = cwtable0[irow][ioffset+1];
            tab11 = cwtable0[irow+1][ioffset+1];
        }
        double tab0 = lerp(frow, tab00, tab10);
        double tab1 = lerp(frow, tab01, tab11);
        //printf("%f of %f %f %f %f\n", frow, tab00, tab01, tab10, tab11);
        //printf("%f of %f %f  /  %f\n", foffset, tab0, tab1, x);

        double val = x*lerp(foffset, tab0, tab1);
        limited->data[(limited->ptr+i) & 63] += val;
        //printf("val0[%i]=%f\n", i, val);
        ioffset += 8;
    }
}

